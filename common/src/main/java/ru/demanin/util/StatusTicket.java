package ru.demanin.util;
/**
 * Перечисление возможных статусов билета в системе.
 * <p>
 * Определяет состояния, в которых может находиться билет ({@link ru.demanin.entity.Ticket})
 * в процессе бронирования и продажи.
 * </p>
 *
 * <p>Доступные статусы:</p>
 * <ul>
 *   <li><b>FREE</b> - билет свободен для бронирования</li>
 *   <li><b>RESERVATION</b> - билет забронирован (временное резервирование)</li>
 * </ul>
 */
public enum StatusTicket {
    FREE, RESERVATION
}
